          <p class="Estilo4"><span class="Estilo3">                Objetivo:</span></p>
          <p class="Estilo5"> Brindar a los Comités Organizadores de Carreras de Endurance Ecuestre un sistema de cómputos que les permita facilitar la carga de datos y realizar los controles correspondientes en forma automatizada. Del mismo modo que la FEA (Federación Ecuestre Argentina) obtenga datos fehacientes y confiables respecto a las carreras realizadas para poder abastecer la base de datos local y cumplimentar los requisitos solicitados por la FEI (Federación Ecuestre Internacional)                 </p>
          <p class="Estilo5">Presentaremos el proyecto en función de la resolución de conflictos de situaciones tratando de evaluar la totalidad de las mismas y dando respuestas a diferentes alternativas. Se proveerá en las carreras de dos personas capacitadas para resolver contingencias y, eventualmente, para la carga de ciertos datos (ejemplo: Vet Control, control central de cómputos, largada/llegada).  Como así también, una vez finalizada la competencia, las planillas oficiales en forma impresa y en formato digital con los resultados y demás información solicitada. Toda la información y resultados, podrán seguirse en forma On line por internet en tanto y en cuanto las condiciones técnicas lo permitan.
            
            Desarrollo:</p>
          <p class="Estilo5"> Módulo Inscripciones y controles previos: <br/>
            Los competidores o comités organizadores dispondrán de un sitio en internet donde realizarán la preinscripción y precarga de los datos. El sistema evaluará y brindará información respecto al estado de los caballos y jinetes en relación a la FEA, a la FEI, y si está en condiciones de participar en esa categoría y competencia.  También en este módulo se realizarán las inscripciones de equipos de ser necesarias.
            Una vez finalizada la fecha prevista en el anteprograma, los responsables del comité organizador deberán confirmar la participación de los inscriptos para que los mismos queden autorizados a competir, para ellos se les brindará un usuario y clave para acceder a un sector restringido.
            A este módulo se le puede incorporar la capacidad de pagos On Line o vía entidades receptoras (Pago Fácil, Rapipago, etc) como también la posibilidad de llevar las cuentas corrientes y caja de inscripción.                 </p>
          <p class="Estilo5">Módulo Recepción de caballos y Vet Check previo: <br/>
            El veterinario en jefe dispondrá de una netbook o Ipad para la carga previa de los estados de caballos, el formato de la planilla deberá ser provisto por la FEA para  homogeneizar este proceso. Estos datos permanecerán en la base de datos para futuras consultas, estadísticas o contrastes.
            Se podrá incorporar a futuro, y según necesidades, firmas electrónicas de los veterinarios habilitados o, en su defecto, un control de identidad por usuario y clave.</p>
          <p class="Estilo5"> Módulo Recepción de jinetes: <br/>
            El Chef Steward dispondrá de una netbook o Ipad para la carga de pesajes y confirmación de la participación, en este acto se le colocará a cada jinete participante (y a dos grooms por caballo) una pulsera RFID (Identificación por radiofrecuencia) que servirá para realizar los controles durante toda la carrera. Al colocarla en la muñeca del jinete, se hará la registración de la misma en el sistema, lo cual convertirá al binomio en ÚNICO.                 </p>
          <p class="Estilo5">Módulo Información de Prensa: <br/>
            Para las competencias Internacionales o locales, se podrá contar con informes impresos y, eventualmente, la contratación de una pantalla que brinde información on line  de: Calcificación de equipos, de jinetes, clasificaciones parciales, estado de binomios por etapas o Vet Gate, etc.</p>
          <p class="Estilo5"> Módulo Largada y Llegada: <br/>
            Para las competencias locales se contará con un reloj electrónico oficial sincronizado con la base de datos. Para las internacionales, además, se podrá alquilar una pantalla que brinde información On Line sobre quién es el próximo jinete a largar y su hora, etc.
            En la largada el reloj tomará la hora oficial de largada en forma automática para todos los competidores (de ser largada colectiva) o, individual (si la largada es por grupos) al pasar el binomio por un lector de RFID con sensibilidad de aprox. 15 mts. y de multi lectura.
            En la llegada, se contará con un sistema de multi lectura de RFID que registrará el horario de cada competidor.                 </p>
          <p class="Estilo5">Módulo de Control Vet Gate: <br/>
            Cada caballo tendrá un “log” único e individual que permanecerá en la base de datos de por vida y que podrá ser consultado en forma permanente vía internet. Esta base, podrá ser alimentada por la FEA previamente a la implementación del sistema y su estructura será consensuada bajo los requerimientos FEI. Los controles de prohibiciones y restricciones se realizarán en función de los datos acumulados aquí, del mismo modo el control de ingreso al recinto dentro del tiempo estipulado en el anteprograma luego de haber traspasado la línea de llegada. 
            Los veterinarios contarán con una (o varias de ser necesario) netbook o Ipad o Hand Reader con capacidades de lecturas RFID para el ingreso por planilla electrónica de los parámetros en la base de log. Depende del tipo de contratación, también se podrá contar con una persona para la carga de los datos. Al finalizar el check, y una vez cargada la carga de datos del caballo, saldrá impresa una tarjeta en formato de etiqueta con la información mínima necesaria para el jinete (hora de partida, estado general del caballo, posición hasta el momento, etc). Recordemos que los datos completos están disponibles en internet. A futuro, podrá sistematizarse según parámetros establecidos las premiaciones de Best Condition basándose en la información volcada en las planillas.
            De los datos establecidos en éste módulo en coordinación con los módulos de control y de largada, se definirán los resultados finales que se verán reflejados en los módulos de establecidos para brindar información.</p>
          <p class="Estilo5"> Módulo de control de paso: <br/>
            En cada circuito se colocarán sensores RFID con multi lectura para el control de paso de los binomios (con un rango aprox. de 15 mts.). Estos sensores enviarán la información (vía WI FI o 3G)  al servidor y se podrá verificar el paso obligado de cada competidor, así como los cálculos correspondientes a velocidad promedio, etc.                 </p>
          <span class="Estilo5">Módulo de Backup de Información y cronómetros: <br/>
          Todos los registros trabajarán en espejo con un servidor local y uno en internet, de esta forma, ante la caída de alguno de los servicios, se podrá seguir haciendo el trabajo en forma remota o tomar las planillas en forma manual y volcarlas más tarde a los servidores. El reloj, tendrá un modo manual apoyado por un reloj sincronizado que permitirá seguir el proceso de tiempos en caso de caída del sistema, pudiendo volver a sincronizarlo al momento de la restauración del mismo.            </span>          <p class="Estilo4">&nbsp;                    </p></th>
